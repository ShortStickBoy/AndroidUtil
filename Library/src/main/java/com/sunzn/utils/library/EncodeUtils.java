package com.sunzn.utils.library;

import android.util.Base64;

public class EncodeUtils {

    private EncodeUtils() {
        throw new RuntimeException("Stub!");
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要编码的字符串
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 编码后的字符串
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64Encode(String input) {
        return base64Encode(input.getBytes());
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要编码的字节数组
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 编码后的字符串
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64Encode(byte[] input) {
        return Base64.encode(input, Base64.NO_WRAP);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要编码的字符串
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 编码后的字符串
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static String base64Encode2String(String input) {
        return Base64.encodeToString(input.getBytes(), Base64.NO_WRAP);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要编码的字节数组
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 编码后的字符串
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static String base64Encode2String(byte[] input) {
        return Base64.encodeToString(input, Base64.NO_WRAP);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 解码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要解码的字符串
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 解码后的字节数组
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64Decode(String input) {
        return Base64.decode(input, Base64.NO_WRAP);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 解码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要解码的字节数组
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64 解码后的字节数组
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64Decode(byte[] input) {
        return Base64.decode(input, Base64.NO_WRAP);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 URL 安全编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要 Base64URL 安全编码的字符串
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64URL 安全编码后的字节数组
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64UrlSafeEncode(String input) {
        return Base64.encode(input.getBytes(), Base64.URL_SAFE);
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：Base64 URL 安全编码
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：input 要 Base64URL 安全编码的字节数组
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Base64URL 安全编码后的字节数组
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static byte[] base64UrlSafeEncode(byte[] input) {
        return Base64.encode(input, Base64.URL_SAFE);
    }

}