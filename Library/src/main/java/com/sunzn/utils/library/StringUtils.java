public class StringUtils {

    private StringUtils() {
        super();
    }

    /**
     * ╔════════════════════════════════════════════════════════════════════════════════════════════
     * ║ 名称：校验字符串是否为空
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 参数：s
     * ╟────────────────────────────────────────────────────────────────────────────────────────────
     * ║ 返回：Boolean
     * ╚════════════════════════════════════════════════════════════════════════════════════════════
     */
    public static boolean isEmpty(String s) {
        return s == null || s.trim().length() == 0;
    }

}
